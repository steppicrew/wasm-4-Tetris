const title_width = 120
const title_height = 65

func render_title() {
	w4.Blit(&title_sprite[0], 20, 20, title_width, title_height, w4.BLIT_1BPP)
	const text string = "Press X to start!"
	w4.Text(text, 80-len(text)*4, 140)
}

func UpdateTitle() {
	if title_active {
		var gamepad = *w4.GAMEPAD1
		if gamepad&w4.BUTTON_1 != 0 {
			title_active = false
			initialize()
		}
	}
}
